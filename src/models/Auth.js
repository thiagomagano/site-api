import mongoose from "mongoose";

const AuthSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model("Auth", AuthSchema);